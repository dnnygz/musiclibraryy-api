async function seed() {
  try {
    console.log('üå± Seeding database...');

    for (const song of seedSongs) {
      await sql`
        INSERT INTO songs (title, artist, album, genre, year, duration, image_url)
        VALUES (${song.title}, ${song.artist}, ${song.album}, ${song.genre}, ${song.year}, ${song.duration}, ${song.image_url})
        ON CONFLICT DO NOTHING
      `;
    }

    console.log(`‚úÖ Database seeded successfully with ${seedSongs.length} songs`);
    process.exit(0);
  } catch (error) {
    console.error('‚ùå Seed failed:', error);
    process.exit(1);
  }
}

